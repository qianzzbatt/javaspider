package model;

public class MoneyModel {	
		private String id;
		private String year;
		private String qian;
		private String songgu;
		private String zhuangu;
		
		public String getId() {
			return id;
		}
		public void setId(String id) {
			this.id = id;
		}
		public String getYear() {
			return year;
		}
		public void setYear(String year) {
			this.year = year;
		}
		public String getQian() {
			return qian;
		}
		public void setQian(String qian) {
			this.qian = qian;
		}
		public String getSonggu() {
			return songgu;
		}
		public void setSonggu(String songgu) {
			this.songgu = songgu;
		}
		public String getZhuangu() {
			return zhuangu;
		}
		public void setZhuangu(String zhuangu) {
			this.zhuangu = zhuangu;
		}
}
